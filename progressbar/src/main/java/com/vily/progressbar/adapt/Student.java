package com.vily.progressbar.adapt;

public class Student {

    private String data;

    private String name;

    public Student() {
    }

    public Student(String data, String name) {
        this.data = data;
        this.name = name;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
